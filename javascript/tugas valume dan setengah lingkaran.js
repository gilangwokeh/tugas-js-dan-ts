//tugas bikin volume dan setengah lingkaran di js
function luasLingkaran(r){
    let hasil = Math.PI * r * r
    console.log(hasil)
}
luasLingkaran(20)


function kelilingLingkaran(r){
    let hasil2 = 2 * Math.PI * r
    console.log(hasil2)
}
kelilingLingkaran(20);

function setengahLingkaran(r){
    let hasil3 = 1/2 * Math.PI * r * r
    console.log(hasil3)
}
setengahLingkaran(20);
